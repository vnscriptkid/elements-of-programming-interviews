import { palindromePartitioning } from "./submit-1";

test("it works", () => {
  const result = palindromePartitioning("0204451881");

  expect(result).toEqual([
    ["0", "2", "0", "4", "4", "5", "1", "8", "8", "1"],
    ["0", "2", "0", "4", "4", "5", "1", "88", "1"],
    ["0", "2", "0", "4", "4", "5", "1881"],
    ["0", "2", "0", "44", "5", "1", "8", "8", "1"],
    ["0", "2", "0", "44", "5", "1", "88", "1"],
    ["0", "2", "0", "44", "5", "1881"],
    ["020", "4", "4", "5", "1", "8", "8", "1"],
    ["020", "4", "4", "5", "1", "88", "1"],
    ["020", "4", "4", "5", "1881"],
    ["020", "44", "5", "1", "8", "8", "1"],
    ["020", "44", "5", "1", "88", "1"],
    ["020", "44", "5", "1881"],
  ]);
});
